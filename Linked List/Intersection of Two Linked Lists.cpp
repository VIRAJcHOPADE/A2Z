class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code here
       unordered_map<int,int> ump;
      Node *temp1=head2;
      int l=0;
      while(temp1)
      {
          ump[temp1->data]=l;
          l++;
          temp1=temp1->next;
      }
      Node *ans=new Node(0);
      Node *temp=ans;
      while(head1)
      {
          if(ump.find(head1->data)!=ump.end())
          {
              Node *var = new Node(head1->data);
              ans->next=var;
              ans=var;
          }
          head1=head1->next;
      }
      return temp->next;
        
    }
};

